def read_anchor(path: Path, *, max_chars: int | None = None, from_end: bool = False) -> str:
    """Read an anchor file with a hard budget so stale corpora cannot hang review calls."""
    text = path.read_text()
    if max_chars is None or len(text) <= max_chars:
        return text
    if from_end:
        return (
            f"[TRUNCATED: showing last {max_chars} of {len(text)} chars from {path.name}]\n\n"
            f"{text[-max_chars:]}"
        )
    return (
        f"{text[:max_chars]}\n\n"
        f"[TRUNCATED: showing first {max_chars} of {len(text)} chars from {path.name}]"
    )


def add_part(parts: list[str], title: str, path: Path, *, max_chars: int | None = None, from_end: bool = False) -> int:
    text = read_anchor(path, max_chars=max_chars, from_end=from_end)
    parts.append(f"# {title}\n\n{text}")
    return len(text)


def take_budget(remaining: int, desired: int) -> int:
    return max(0, min(remaining, desired))


