def load_config() -> dict:
    """Read render-config.yaml. Tiny YAML — sufficient subset, no external dep."""
    path = META_DIR / "render-config.yaml"
    if not path.exists():
        return DEFAULT_CONFIG
    try:
        text = path.read_text(encoding="utf-8")
    except OSError:
        return DEFAULT_CONFIG
    cfg = dict(DEFAULT_CONFIG)
    sections: list[str] = []
    row_caps: dict[str, int] = {}
    decisions_cfg: dict[str, int] = {}
    mode = None
    for raw in text.splitlines():
        line = raw.rstrip()
        if not line.strip() or line.lstrip().startswith("#"):
            continue
        # Top-level mapping starters
        stripped = line.lstrip()
        indent = len(line) - len(stripped)
        if indent == 0 and stripped.endswith(":"):
            mode = stripped[:-1]
            continue
        if indent == 0 and ":" in stripped:
            k, _, v = stripped.partition(":")
            v = v.strip()
            if v.lower() in ("true", "false"):
                cfg[k.strip()] = v.lower() == "true"
            mode = None
            continue
        if mode == "sections" and stripped.startswith("- "):
            sections.append(stripped[2:].strip())
        elif mode == "row_caps" and ":" in stripped:
            k, _, v = stripped.partition(":")
            try:
                row_caps[k.strip()] = int(v.strip())
            except ValueError:
                pass
        elif mode == "decisions" and ":" in stripped:
            k, _, v = stripped.partition(":")
            try:
                decisions_cfg[k.strip()] = int(v.strip())
            except ValueError:
                pass
    if sections:
        cfg["sections"] = sections
    if row_caps:
        cfg["row_caps"] = row_caps
    if decisions_cfg:
        cfg["decisions"] = decisions_cfg
    return cfg


def _bar(filled: int, total: int, width: int = 10) -> str:
    if total <= 0:
        return "░" * width
    pct = min(1.0, filled / total)
    n = int(round(pct * width))
    return "▓" * n + "░" * (width - n)


def _decision_icon(rule: str) -> str:
    if "overdue" in rule:
        return "⚠"
    if "approaching" in rule:
        return "🟡"
    if rule.startswith("metric"):
        return "📊"
    if rule.startswith("content"):
        return "📝"
    if rule.startswith("launch"):
        return "🚀"
    if rule.startswith("prospect"):
        return "💼"
    if rule.startswith("bd"):
        return "🤝"
    return "•"


