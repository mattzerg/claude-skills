def compact_markdown_for_quick_review(content: str, max_chars: int = 18000) -> str:
    """Keep quick reviews fast by preserving the blog body plus social/checklist surfaces."""
    if len(content) <= max_chars:
        return content
    sections = []
    markers = [
        "\n## LinkedIn",
        "\n## Twitter",
        "\n## Thread",
        "\n## Single",
        "\n## Pre-publish checklist",
        "\n## Distribution Checklist",
    ]
    cut = len(content)
    for marker in markers:
        idx = content.find(marker)
        if idx != -1:
            cut = min(cut, idx)
    body_budget = max_chars // 2
    sections.append(content[:body_budget].rstrip())
    for marker in markers:
        idx = content.find(marker)
        if idx != -1:
            sections.append(content[idx : idx + 2200].rstrip())
    compacted = "\n\n[...middle truncated for quick copyedit...]\n\n".join(sections)
    return compacted[:max_chars]


