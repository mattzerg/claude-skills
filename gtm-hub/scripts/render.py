def render_open_decisions(entities: list[dict], today: dt.date) -> list[str]:
    """Render human-curated open decisions as a numbered briefing.

    Report style: each decision is a numbered section (#1 of N) with urgency
    band, headline title, question, context, A/B/C options with inline
    `gtm decide` commands, and downstream-impact list.
    """
    decs = [
        e for e in entities
        if e["type"] == "decision" and e["status"] in ("open", "deferred")
    ]
    if not decs:
        return []

    def _urgency_key(e: dict) -> tuple:
        d = e.get("deadline") or ""
        try:
            dl = dt.date.fromisoformat(d) if d else None
        except (ValueError, TypeError):
            dl = None
        if dl:
            return (0, (dl - today).days, e.get("id") or "")
        unblocks = e.get("unblocks") or []
        return (1, -(len(unblocks) if isinstance(unblocks, list) else 0), e.get("id") or "")

    decs.sort(key=_urgency_key)
    n = len(decs)
    lines = [
        "## 🤔 Open decisions — your plate",
        "",
        f"_{n} decision{'s' if n != 1 else ''} awaiting your call. "
        "Each one is below — review options, then run the `gtm decide` command "
        "for your choice (or `gtm defer <id>` to kick the can)._",
        "",
        "---",
        "",
    ]

    for i, e in enumerate(decs[:5], 1):
        eid = e.get("id") or ""
        title = e.get("title") or eid
        deadline_str = e.get("deadline") or ""
        unblocks = e.get("unblocks") or []
        unblocks_n = len(unblocks) if isinstance(unblocks, list) else 0

        # Urgency band
        urgency_icon = "⚪"
        urgency_label = "no deadline"
        if deadline_str:
            try:
                dl = dt.date.fromisoformat(deadline_str)
                delta = (dl - today).days
                long_date = dl.strftime("%b %-d, %Y")
                if delta < 0:
                    urgency_icon = "🔴"
                    urgency_label = f"OVERDUE by {-delta}d (deadline was {long_date})"
                elif delta == 0:
                    urgency_icon = "🔴"
                    urgency_label = f"DUE TODAY ({long_date})"
                elif delta <= 7:
                    urgency_icon = "🟠"
                    urgency_label = f"DUE {long_date} ({delta} day{'s' if delta != 1 else ''})"
                elif delta <= 21:
                    urgency_icon = "🟡"
                    urgency_label = f"due {long_date} ({delta} days)"
                else:
                    urgency_icon = "🟢"
                    urgency_label = f"due {long_date} ({delta} days)"
            except ValueError:
                urgency_label = f"deadline {deadline_str}"

        status_pill = "" if e["status"] == "open" else f" · _[{e['status']}]_"
        unblocks_str = f" · unblocks {unblocks_n} downstream" if unblocks_n else ""

        # Header: "DECISION 1 of 3 · 🟠 DUE May 19 (8 days) · unblocks 4 downstream"
        lines.append(
            f"### DECISION {i} of {n} · {urgency_icon} {urgency_label}{unblocks_str}{status_pill}"
        )
        lines.append("")
        # Title as a level-4 head — big and readable
        lines.append(f"#### {title}")
        lines.append("")

        # Question
        question = e.get("question") or ""
        if question:
            lines.append("**The call.**")
            for q_line in str(question).strip().splitlines():
                lines.append(q_line)
            lines.append("")

        # Context
        context = e.get("context") or ""
        if context:
            lines.append("**Why it matters.**")
            for c_line in str(context).strip().splitlines():
                lines.append(c_line)
            lines.append("")

        # Options as A / B / C
        options = e.get("options") or []
        if isinstance(options, list) and options:
            lines.append("**Options.**")
            lines.append("")
            for opt_i, opt in enumerate(options):
                if not isinstance(opt, dict):
                    continue
                letter = chr(ord("A") + opt_i) if opt_i < 26 else f"#{opt_i+1}"
                key = opt.get("key", "?")
                label = opt.get("label", key)
                lines.append(f"**{letter}.** {label}")
                impl = opt.get("implications")
                if impl:
                    for impl_line in str(impl).strip().splitlines():
                        lines.append(f"   {impl_line}")
                lines.append(f"   → `gtm decide {eid} {key}`")
                lines.append("")

        # Downstream impact — auto-derived projects this decision blocks
        blocks_projects = e.get("blocks_projects") or []
        if isinstance(blocks_projects, list) and blocks_projects:
            lines.append(f"**Blocks {len(blocks_projects)} project{'s' if len(blocks_projects) != 1 else ''}:**")
            for bp in blocks_projects:
                target = bp.get("target_date") or "no deadline"
                rag = bp.get("rag") or ""
                icon = "🔴" if bp.get("status") == "blocked" else {"red": "🔴", "amber": "🟡", "green": "🟢"}.get(rag, "⚪")
                lines.append(f"- {icon} **{bp.get('title', bp.get('id'))}** (target {target})")
            lines.append("")
        # Hand-written unblocks (often non-project artifacts)
        if isinstance(unblocks, list) and unblocks:
            lines.append("**Other downstream:**")
            for u in unblocks:
                lines.append(f"- `{u}`")
            lines.append("")

        # Defer escape hatch
        lines.append(f"_Not ready? `gtm defer {eid}` to kick this back._")
        lines.append("")
        lines.append("---")
        lines.append("")

    if len(decs) > 5:
        lines.append(
            f"_+{len(decs) - 5} more open decisions — see `decisions/` directory._"
        )
        lines.append("")
    return lines


def render_decisions(decisions: list[dict], limit: int = DECISIONS_VISIBLE_DEFAULT) -> list[str]:
    """Diversify by rule so ≤limit visible per `feedback_dashboard_must_drive_action.md`.

    Drops `kind == "backlog"` rules — those surface in `render_debt()` so the
    decisions panel only carries actionable this-week calls.
    """
    from lib.rules import diversify
    from act import hint_for_decision

    lines = ["## ▶ Triage (rule-derived signals)"]
    if not decisions:
        lines += ["", "_All rule-derived signals quiet. 🟢_", ""]
        return lines

    # Filter out:
    #   - decision.open (covered in full above by render_open_decisions)
    #   - kind=backlog (covered separately by render_debt)
    decisions = [
        d for d in decisions
        if d.get("rule") != "decision.open" and d.get("kind", "decision") != "backlog"
    ]
    if not decisions:
        return ["## ▶ Triage", "", "_All rule-derived signals quiet. 🟢_", ""]
    visible = diversify(decisions, limit=limit)
    lines += [""]
    for i, (d, siblings, names) in enumerate(visible, 1):
        icon = _decision_icon(d["rule"])
        # System rules have no entity file — omit the link
        if d.get("entity_path"):
            ref = f" — [`{d['entity_id']}`]({_relpath(d['entity_path'])})"
        else:
            ref = ""
        if siblings:
            shown = ", ".join(f"`{n}`" for n in names)
            extra = f" +{siblings - len(names)}" if siblings > len(names) else ""
            tail = f" _(+{siblings} more: {shown}{extra})_"
        else:
            tail = ""
        hint = hint_for_decision(d.get("rule", ""), d.get("entity_id", ""))
        hint_str = f" → `{hint}`" if hint else ""
        lines.append(f"{i}. {icon} {d['message']}{ref}{tail}{hint_str}")
    rolled_up_other = len(decisions) - sum(1 + s for _, s, _ in visible)
    if rolled_up_other > 0:
        lines.append("")
        lines.append(f"_+{rolled_up_other} more in `_meta/decisions.json` (other rules)._")
    lines.append("")
    return lines


def render_debt(decisions: list[dict], limit: int = 3) -> list[str]:
    """Render `kind=backlog` rules as a separate Measurement-debt panel.

    Same diversification pass as the decisions panel (one row per rule, sibling
    count for the rest) but visually demoted so it doesn't crowd out this-week
    calls. Hidden entirely when there's no backlog signal.
    """
    from lib.rules import diversify

    backlog = [d for d in decisions if d.get("kind") == "backlog"]
    if not backlog:
        return []
    visible = diversify(backlog, limit=limit)
    lines = [
        "## 📚 Measurement debt",
        "",
        "_Known cleanup epics — not this-week decisions. Surfaced here so they "
        "don't crowd the triage panel above._",
        "",
    ]
    for d, siblings, names in visible:
        icon = _decision_icon(d["rule"])
        if d.get("entity_path"):
            ref = f" — [`{d['entity_id']}`]({_relpath(d['entity_path'])})"
        else:
            ref = ""
        if siblings:
            shown = ", ".join(f"`{n}`" for n in names)
            extra = f" +{siblings - len(names)}" if siblings > len(names) else ""
            tail = f" _(+{siblings} more: {shown}{extra})_"
        else:
            tail = ""
        lines.append(f"- {icon} {d['message']}{ref}{tail}")
    rolled_up_other = len(backlog) - sum(1 + s for _, s, _ in visible)
    if rolled_up_other > 0:
        lines.append("")
        lines.append(f"_+{rolled_up_other} more backlog rows in `_meta/decisions.json`._")
    lines.append("")
    return lines


def _relpath(p: str) -> str:
    """Return a Growth/-relative path for a markdown link.

    Entities are loaded from the local mirror (read-tolerant) but the README
    lives in canonical iCloud — so links must be relative to canonical Growth/.
    We compare the trailing components: if the path includes `/Growth/<rest>`,
    return `<rest>`, regardless of whether the leading dirs are iCloud or mirror.
    """
    if not p:
        return p
    # Strip everything up to and including the rightmost `/Growth/`
    marker = "/Growth/"
    idx = p.rfind(marker)
    if idx >= 0:
        return p[idx + len(marker):]
    # Fallback: try canonical relative_to
    try:
        return str(Path(p).resolve().relative_to(GROWTH_DIR.resolve()))
    except (ValueError, OSError):
        return p


def render_dashboard(entities: list[dict], today: dt.date) -> list[str]:
    """Top-level at-a-glance state — replaces phase_status."""
    day = (today - PHASE_1_START).days + 1
    if day < 1:
        day = 1
    if day > PHASE_1_DAYS:
        day = PHASE_1_DAYS
    days_left = max(0, PHASE_1_DAYS - day)

    # Pull entity-set state
    exps = [e for e in entities if e["type"] == "experiment"]
    running = [e for e in exps if e["status"] == "running"]
    concluded = [e for e in exps if e["status"] in ("won", "killed", "inconclusive")]

    content = [e for e in entities if e["type"] == "content"]
    content_drafted = [e for e in content if e["status"] in ("drafted", "reviewed", "scheduled")]
    content_shipped = [e for e in content if e["status"] in ("published", "distributed")]

    bd = [e for e in entities if e["type"] == "bd_target"]
    bd_active = [e for e in bd if e["status"] in ("outreach", "engaged")]

    prospects = [e for e in entities if e["type"] == "prospect"]
    prospects_qualified_plus = [e for e in prospects if e["status"] in ("qualified", "scoped", "proposal-out", "won")]
    prospects_inbound = [e for e in prospects if e["status"] == "inbound"]

    launches = [e for e in entities if e["type"] == "launch"]
    launches_active = [e for e in launches if e["status"] in ("drafting", "ready", "scheduled")]

    metrics = [e for e in entities if e["type"] == "metric"]
    metrics_instrumented = [m for m in metrics if m["status"] in ("instrumented", "monitored")]
    metrics_red = [m for m in metrics if m["status"] == "not-instrumented"]

    open_decs = [e for e in entities if e["type"] == "decision" and e["status"] in ("open", "deferred")]
    open_decs_urgent = [
        e for e in open_decs
        if e.get("deadline") and parse_date(e.get("deadline"))
        and (parse_date(e.get("deadline")) - today).days <= 7
    ]

    projects = [e for e in entities if e["type"] == "project"]
    projects_red = [p for p in projects if (p.get("effective_rag") or "").lower() == "red"]
    projects_amber = [p for p in projects if (p.get("effective_rag") or "").lower() == "amber"]
    projects_green = [p for p in projects if (p.get("effective_rag") or "").lower() == "green"]

    nsm_lines = []
    for slug in ("wapw", "qpv"):
        m = next((mm for mm in metrics if mm["id"] == slug), None)
        if not m:
            continue
        title = m.get("title") or slug.upper()
        val = m.get("value")
        if val in (None, ""):
            nsm_lines.append(f"  {title[:48]:<48} ░░░░░░░░░░  NOT INSTRUMENTED")
        else:
            tgt = m.get("target") or "—"
            try:
                bar = _bar(float(val), float(tgt) if tgt not in (None, "—", "") else float(val) * 2)
            except (ValueError, TypeError):
                bar = "░" * 10
            nsm_lines.append(f"  {title[:48]:<48} {bar}  {val} / target {tgt}")

    lines = [
        "## ⚡ Dashboard",
        "",
        "```",
        f"  Phase 1 — Growth Program            {_bar(day, PHASE_1_DAYS)}  Day {day}/{PHASE_1_DAYS} · {days_left}d left",
        "",
        "  NORTH-STAR METRICS",
    ]
    if nsm_lines:
        lines.extend(nsm_lines)
    else:
        lines.append("    (no NSM entities found)")
    lines.append("")
    lines.append("  PIPELINE COUNTS                          ACTIVE       TOTAL")
    lines.append(f"  🤔 Open decisions                          {len(open_decs):<4}         {len(open_decs):<4}" + (f"  · ⚠ {len(open_decs_urgent)} urgent" if open_decs_urgent else ""))
    lines.append(f"  📋 Projects                                {len(projects) - len([p for p in projects if p['status'] in ('shipped', 'canceled', 'paused')]):<4}         {len(projects):<4}" + (f"  · 🔴 {len(projects_red)} red" if projects_red else "") + (f"  · 🟡 {len(projects_amber)} amber" if projects_amber else ""))
    lines.append(f"  🧪 Experiments                             {len(running):<4}         {len(exps):<4}" + (f"  · ✅ {len(concluded)} concluded" if concluded else ""))
    lines.append(f"  📝 Content (drafted+reviewed+scheduled)    {len(content_drafted):<4}         {len(content):<4}" + (f"  · ✅ {len(content_shipped)} shipped" if content_shipped else ""))
    lines.append(f"  🤝 BD (outreach+engaged)                   {len(bd_active):<4}         {len(bd):<4}")
    lines.append(f"  💼 Solutions (qualified+)                  {len(prospects_qualified_plus):<4}         {len(prospects):<4}" + (f"  · 📨 {len(prospects_inbound)} inbound" if prospects_inbound else ""))
    lines.append(f"  🚀 Launches                                {len(launches_active):<4}         {len(launches):<4}")
    lines.append(f"  📊 Metrics instrumented                    {len(metrics_instrumented):<4}         {len(metrics):<4}" + (f"  · 🔴 {len(metrics_red)} red" if metrics_red else ""))
    lines.append("```")
    lines.append("")
    # Red flags as a one-line callout
    flags = []
    if metrics_red:
        flags.append(f"{len(metrics_red)} metrics not instrumented (incl. {', '.join(m['id'] for m in metrics_red if m['id'] in ('wapw', 'qpv'))})")
    if open_decs_urgent:
        flags.append(f"{len(open_decs_urgent)} decision{'s' if len(open_decs_urgent) != 1 else ''} due in ≤7d")
    if projects_red:
        flags.append(f"{len(projects_red)} project{'s' if len(projects_red) != 1 else ''} red/blocked")
    if flags:
        lines.append(f"🚨 **Red flags.** {' · '.join(flags)}")
        lines.append("")
    return lines


def render_projects(entities: list[dict], today: dt.date) -> list[str]:
    """Project tracker — one row per cross-entity project."""
    projects = [e for e in entities if e["type"] == "project"]
    if not projects:
        return []

    # Sort: red first, then amber, then green, by deadline proximity within each band
    def _key(p):
        rag_order = {"red": 0, "amber": 1, "green": 2}
        rag = rag_order.get((p.get("effective_rag") or p.get("rag") or "").lower(), 3)
        td = p.get("target_date") or ""
        try:
            dl = dt.date.fromisoformat(td) if td else None
        except (ValueError, TypeError):
            dl = None
        deadline_sort = (dl - today).days if dl else 9999
        return (rag, deadline_sort, p.get("id") or "")
    projects.sort(key=_key)

    lines = ["## 📋 Projects — in flight", ""]
    active_projects = [p for p in projects if p["status"] not in ("shipped", "canceled", "paused")]
    inactive = [p for p in projects if p["status"] in ("shipped", "canceled", "paused")]
    lines.append(f"_{len(active_projects)} active · {len(inactive)} closed/paused_")
    lines.append("")
    lines.append("| RAG | Project | Owner | Target | Status | Progress | Blockers |")
    lines.append("|-----|---------|-------|--------|--------|----------|----------|")
    for p in active_projects:
        # Prefer auto-derived effective_rag; fall back to manual
        rag = (p.get("effective_rag") or p.get("rag") or "").lower()
        rag_icon = {"red": "🔴", "amber": "🟡", "green": "🟢"}.get(rag, "⚪")
        title = (p.get("title") or p.get("id") or "")[:65]
        owner = p.get("owner") or "—"
        target = p.get("target_date") or "—"
        status = p.get("status") or "—"
        prog = p.get("progress_pct")
        prog_str = f"{prog}%" if prog is not None else "—"
        blockers = p.get("blockers") or []
        blockers_n = len(blockers) if isinstance(blockers, list) else 0
        blockers_str = f"{blockers_n} 🚧" if blockers_n else "—"
        lines.append(f"| {rag_icon} | **{title}** | {owner} | {target} | `{status}` | {prog_str} | {blockers_str} |")
    lines.append("")
    # Per-project detail blocks for red/amber
    flagged = [
        p for p in active_projects
        if (p.get("effective_rag") or p.get("rag") or "").lower() in ("red", "amber")
    ]
    if flagged:
        lines.append("### Project detail (red / amber)")
        lines.append("")
        for p in flagged[:6]:
            title = p.get("title") or p["id"]
            rag = (p.get("effective_rag") or p.get("rag") or "").lower()
            rag_icon = {"red": "🔴", "amber": "🟡", "green": "🟢"}.get(rag, "⚪")
            target = p.get("target_date") or "no deadline"
            lines.append(f"**{rag_icon} {title}** — `{p['status']}` · target {target}")
            summary = p.get("status_summary")
            if summary:
                for s_line in str(summary).strip().splitlines():
                    lines.append(f"> {s_line}")
            # Auto-derived blockers (live state of linked entities)
            derived = p.get("derived_blockers") or []
            if isinstance(derived, list) and derived:
                lines.append("")
                lines.append("Auto-detected blockers (live):")
                for db in derived:
                    lines.append(f"- 🔴 {db}")
            blockers = p.get("blockers") or []
            if isinstance(blockers, list) and blockers:
                lines.append("")
                lines.append("Notes (manual):")
                for b in blockers:
                    lines.append(f"- 🚧 {b}")
            linked = p.get("linked_entities") or []
            if isinstance(linked, list) and linked:
                lines.append("")
                lines.append(f"Linked entities ({len(linked)}):")
                for le in linked[:5]:
                    lines.append(f"- `{le}`")
                if len(linked) > 5:
                    lines.append(f"- _+{len(linked) - 5} more_")
            lines.append("")
            lines.append("---")
            lines.append("")
    return lines


def render_phase_status(entities: list[dict], today: dt.date) -> list[str]:
    day = (today - PHASE_1_START).days + 1
    if day < 1:
        day = 1
    if day > PHASE_1_DAYS:
        day = PHASE_1_DAYS
    exps = [e for e in entities if e["type"] == "experiment"]
    running = [e for e in exps if e["status"] == "running"]
    concluded = [e for e in exps if e["status"] in ("won", "killed", "inconclusive")]
    metrics = [e for e in entities if e["type"] == "metric"]
    instrumented = [m for m in metrics if m["status"] in ("instrumented", "monitored")]

    nsm_lines = []
    for m in metrics:
        if (m.get("category") or "").lower() == "nsm" or (m.get("id") or "") in ("wapw", "qpv"):
            val = m.get("value")
            tgt = m.get("target")
            if val in (None, ""):
                bar = "░" * 10
                label = "not instrumented"
            else:
                try:
                    bar = _bar(float(val), float(tgt) if tgt not in (None, "") else float(val) * 2)
                except (ValueError, TypeError):
                    bar = "░" * 10
                label = f"{val}" + (f" / target {tgt}" if tgt not in (None, "") else "")
            nsm_lines.append(f"NSM {m.get('title') or m.get('id'):<24} {bar}  {label}")

    lines = ["## 📊 Where we are"]
    lines += [""]
    lines += ["```"]
    lines.append(f"Phase 1 progress  {_bar(day, PHASE_1_DAYS)}  Day {day}/{PHASE_1_DAYS}")
    lines.append(f"Experiments       {_bar(len(running), max(5, len(running) + len(concluded)))}  {len(running)} running · {len(concluded)} concluded · {len(exps)} total")
    if metrics:
        lines.append(f"Metric coverage   {_bar(len(instrumented), max(1, len(metrics)))}  {len(instrumented)}/{len(metrics)} instrumented")
    # Zergboard linkage coverage — non-workstream entities only (workstreams aren't card-backed)
    linkable = [e for e in entities if e["type"] != "workstream"]
    linked = [e for e in linkable if e.get("zergboard_card")]
    if linkable:
        lines.append(f"Zergboard linked  {_bar(len(linked), len(linkable))}  {len(linked)}/{len(linkable)} entities have cards")
    for n in nsm_lines:
        lines.append(n)
    lines += ["```", ""]
    return lines


def _section_table(rows: list[dict], cols: list[tuple[str, str, int]], empty_msg: str) -> list[str]:
    if not rows:
        return [f"_{empty_msg}_", ""]
    header = "| " + " | ".join(name for name, _, _ in cols) + " |"
    sep = "|" + "|".join("-" * (max(3, w + 2)) for _, _, w in cols) + "|"
    out = [header, sep]
    for r in rows:
        cells = []
        for _, key, _ in cols:
            v = r.get(key)
            cells.append(str(v) if v not in (None, "") else "—")
        out.append("| " + " | ".join(cells) + " |")
    out.append("")
    return out


def _counts(entities: list[dict], etype: str) -> dict[str, int]:
    out: dict[str, int] = {}
    for e in entities:
        if e["type"] != etype:
            continue
        s = e.get("status") or "—"
        out[s] = out.get(s, 0) + 1
    return out


def _format_counts(counts: dict[str, int]) -> str:
    if not counts:
        return "none"
    return " · ".join(f"{v} {k}" for k, v in sorted(counts.items(), key=lambda x: -x[1]))


