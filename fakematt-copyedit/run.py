def review_basename(draft_path: Path) -> str:
    """Compute a collision-safe basename for the review file.

    Two drafts with the same filename in sibling directories (e.g.
    `compare/jira.md` and `integrations/jira.md`) used to collide on
    `jira.review.md` and silently overwrite each other. Prefix the
    basename with the parent directory name when the parent looks
    like a content-surface segment (compare, integrations, blog,
    posts, etc.). Default behavior is unchanged when there's no
    collision risk.
    """
    SURFACE_PARENTS = {
        "compare", "comparisons", "integrations", "integration",
        "blog", "posts", "drafts", "guides",
    }
    base = draft_path.stem.replace(" ", "_")
    parent = draft_path.parent.name
    if parent in SURFACE_PARENTS:
        return f"{parent}-{base}"
    return base


def write_review(out_dir: Path, draft_path: Path, review_md: str) -> tuple[Path, Path | None]:
    """Write the full review file. Optionally split out the interview queue if non-trivial."""
    out_dir.mkdir(parents=True, exist_ok=True)
    base = review_basename(draft_path)
    review_path = out_dir / f"{base}.review.md"
    review_path.write_text(review_md)

    interview_path: Path | None = None
    marker = "## Interview queue"
    if marker in review_md:
        body_after = review_md.split(marker, 1)[1].lstrip("\n")
        if "No interview items" not in body_after.split("\n", 1)[0] and "No interview items" not in body_after[:200]:
            interview_path = out_dir / f"{base}.interview.md"
            interview_path.write_text(
                f"# Interview queue: {draft_path.stem}\n\n"
                f"**Source review:** {review_path}\n"
                f"**Source draft:** {draft_path}\n\n"
                f"---\n\n{marker}{body_after}"
            )

    # Best-effort: append findings to the per-article ledger if the draft
    # resolves to a tracked content slug.
    try:
        import sys as _sys
        _sys.path.insert(0, str(Path.home() / ".config" / "zerg" / "lib"))
        import article_lock as _al  # type: ignore
        slug = _al.file_path_to_slug(str(draft_path))
        if slug:
            n = _al.ingest_review_file(review_path, slug, reviewer="matt")
            if n:
                print(f"[fakematt-copyedit] appended {n} finding(s) to feedback ledger for slug={slug}",
                      file=sys.stderr)
    except Exception as _e:  # noqa: BLE001
        print(f"[fakematt-copyedit] warn: ledger ingest failed: {_e}", file=sys.stderr)

    return review_path, interview_path


def write_session_summary(out_dir: Path, results: list[dict], model: str, started: float) -> Path:
    duration = time.time() - started
    today = dt.date.today().isoformat()
    lines = [
        "# Fake Matt Copyedit — session summary",
        "",
        f"**Date:** {today}",
        f"**Model:** {model}",
        f"**Duration:** {duration:.1f}s",
        f"**Drafts reviewed:** {len(results)}",
        "",
        "## Files",
        "",
    ]
    for r in results:
        lines.append(f"### {r['draft'].name}")
        lines.append(f"- Source: `{r['draft']}`")
        lines.append(f"- Review: `{r['review']}`")
        if r.get("interview"):
            lines.append(f"- Interview queue: `{r['interview']}`  ⚠️ has discussion items")
        else:
            lines.append("- Interview queue: none (all findings HIGH/MEDIUM)")
        lines.append("")
    summary = out_dir / "session-summary.md"
    summary.write_text("\n".join(lines))
    return summary


def maybe_pdf_and_open(files: list[Path], open_files: bool) -> None:
    """Default: do nothing. Caller must opt-in via --open. Per Matt:
    review/interview/summary files are read by Claude and are not for
    auto-opening — they clutter the screen with duplicate windows."""
    if not open_files:
        return
    if not PDF_SCRIPT.exists():
        subprocess.run(["open"] + [str(f) for f in files])
        return
    subprocess.run(["python3", str(PDF_SCRIPT)] + [str(f) for f in files])


def main() -> int:
    p = argparse.ArgumentParser(description="Fake Matt copyedit pass on prose drafts.")
    p.add_argument("drafts", nargs="+", help="Markdown draft paths")
    p.add_argument("--out-dir", default=str(DEFAULT_OUT), help="Output directory")
    p.add_argument("--model", default=None,
                   help=f"Claude model id (default: aitr pick, falling back to {DEFAULT_MODEL})")
    p.add_argument("--model-timeout", type=int, default=DEFAULT_MODEL_TIMEOUT, help="Seconds to wait for the model call before failing")
    p.add_argument("--max-anchor-chars", type=int, default=DEFAULT_MAX_ANCHOR_CHARS, help="Maximum total anchor characters to include before the draft")
    p.add_argument("--no-pdf", action="store_true", help="Deprecated; default is no-open. Kept for backward compat.")
    p.add_argument("--open", action="store_true", help="Open session-summary + review + interview files (off by default — Matt does NOT want these auto-opened).")
    p.add_argument("--quick", action="store_true", help="Skip social-variant deep dive")
    args = p.parse_args()

    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)
    started = time.time()

    # Model resolution: explicit --model > aitr pick > DEFAULT_MODEL (loud fallback).
    if args.model is None:
        total_draft_chars = sum(
            len(Path(raw).read_text(errors="replace")) for raw in args.drafts if Path(raw).exists()
        )
        args.model = aitr_model_or(
            DEFAULT_MODEL,
            task_kind="prose-review",
            caller="fakematt-copyedit",
            quality_floor="high-stakes",
            artifact_size_tokens=total_draft_chars // 4,
        )

    max_anchor_chars = args.max_anchor_chars
    if args.quick and max_anchor_chars == DEFAULT_MAX_ANCHOR_CHARS:
        max_anchor_chars = 10000
    anchors = load_anchors(quick=args.quick, max_anchor_chars=max_anchor_chars)
    results = []
    sent_log_path = Path(__file__).parent / "sent-log.jsonl"
    for raw in args.drafts:
        path = Path(raw)
        if not path.exists():
            print(f"MISSING: {path}", file=sys.stderr)
            continue
        print(f"[copyedit] reviewing {path.name}…", file=sys.stderr)
        # Snapshot input content BEFORE review for the learning loop
        try:
            input_content = path.read_text()
        except Exception:
            input_content = ""
        try:
            review_md = review_one(path, anchors, args.model, args.model_timeout, quick=args.quick)
        except Exception as e:
            print(f"[copyedit] model call failed for {path}: {e}", file=sys.stderr)
            continue
        review_path, interview_path = write_review(out_dir, path, review_md)
        results.append({"draft": path, "review": review_path, "interview": interview_path})
        print(f"  → {review_path}", file=sys.stderr)
        if interview_path:
            print(f"  → {interview_path}  (LOW-confidence items)", file=sys.stderr)
        # Log for learn.py — track if Matt edits the input draft after seeing the review
        try:
            import json as _json
            with open(sent_log_path, "a") as f:
                f.write(_json.dumps({
                    "ts": dt.datetime.now().strftime("%Y%m%dT%H%M%S"),
                    "input_path": str(path.resolve()),
                    "input_content_at_review": input_content,
                    "review_path": str(review_path),
                    "model": args.model,
                    "checked": False,
                }) + "\n")
        except Exception as e:
            print(f"[sent-log] {e}", file=sys.stderr)

    if not results:
        print("No reviews produced.", file=sys.stderr)
        return 1

    summary = write_session_summary(out_dir, results, args.model, started)
    print(f"\n[copyedit] summary: {summary}", file=sys.stderr)

    # Open everything in Preview as one window.
    files_to_open: list[Path] = [summary]
    for r in results:
        files_to_open.append(r["review"])
        if r.get("interview"):
            files_to_open.append(r["interview"])
    maybe_pdf_and_open(files_to_open, args.open)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
