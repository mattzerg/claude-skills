def render(index_payload: dict, decisions_payload: dict) -> str:
    today = dt.date.today()
    entities = index_payload.get("entities", [])
    decisions = decisions_payload.get("decisions", [])
    config = load_config()
    decisions_limit = config.get("decisions", {}).get("visible_count", DECISIONS_VISIBLE_DEFAULT)
    hide_healthy = bool(config.get("hide_healthy_sections", True))

    section_renderers = {
        "dashboard": lambda: render_dashboard(entities, today),
        "open_decisions": lambda: render_open_decisions(entities, today),
        "projects": lambda: render_projects(entities, today),
        "decisions": lambda: render_decisions(decisions, limit=decisions_limit),
        "debt": lambda: render_debt(decisions),
        "phase_status": lambda: render_phase_status(entities, today),
        "experiments": lambda: render_experiments(entities),
        "content": lambda: render_content(entities),
        "bd": lambda: render_bd(entities, today),
        "prospects": lambda: render_prospects(entities),
        "launches": lambda: render_launches(entities),
        "themes": lambda: render_themes(entities),
        "workstreams": lambda: render_workstreams(entities),
        "dogfood": lambda: render_dogfood(entities),
        "measurement": lambda: render_measurement(entities),
        "crosslinks": lambda: render_crosslinks(),
    }

    # Hide healthy: drop a section if its entity_type has zero non-planned entities.
    # (Crosslinks and decisions/phase_status are never hidden.)
    healthy_check_type = {
        "experiments": "experiment",
        "content": "content",
        "publishing": "publishing",
        "bd": "bd_target",
        "prospects": "prospect",
        "launches": "launch",
        "themes": "theme",
        "workstreams": "workstream",
    }
    inactive_status = {
        "experiment": {"won", "killed", "inconclusive"},
        "content": {"published", "distributed", "parked"},
        "publishing": {"published", "distributed", "archived"},
        "bd_target": {"planned", "paused", "closed-won", "closed-lost"},
        "prospect": {"won", "lost"},
        "launch": {"shipped", "parked"},
        "theme": {"retired"},
        "workstream": {"parked", "stale"},
    }

    parts: list[str] = []
    parts += render_header(today)
    for section in config.get("sections", DEFAULT_CONFIG["sections"]):
        renderer = section_renderers.get(section)
        if not renderer:
            continue
        if hide_healthy and section in healthy_check_type:
            etype = healthy_check_type[section]
            active = [
                e for e in entities
                if e["type"] == etype and e.get("status") not in inactive_status.get(etype, set())
            ]
            if not active:
                continue
        parts += renderer()
    return "\n".join(parts).rstrip() + "\n"


def main() -> int:
    # Resolve index/decisions via staging→iCloud→mirror cascade so this
    # works under launchd TCC immediately after index.py + decisions.py
    # stage their fresh outputs.
    idx_text = read_growth_file("_meta/index.json")
    if idx_text is None:
        print("missing _meta/index.json — run `gtm-hub regenerate` first", file=sys.stderr)
        return 1
    idx = json.loads(idx_text)
    dec_text = read_growth_file("_meta/decisions.json")
    dec = json.loads(dec_text) if dec_text else {"decisions": []}
    content = render(idx, dec)
    # Only write if changed
    existing = read_growth_file("README.md")
    if existing == content:
        print(f"unchanged: README.md")
        return 0
    written = write_growth_file("README.md", content)
    print(f"wrote {written} ({len(content)} bytes)")
    return 0


if __name__ == "__main__":
    sys.exit(main())
